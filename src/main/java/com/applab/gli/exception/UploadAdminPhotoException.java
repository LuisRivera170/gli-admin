package com.applab.gli.exception;

public class UploadAdminPhotoException extends Exception {

    public UploadAdminPhotoException() {
    }

    public UploadAdminPhotoException(String message) {
        super(message);
    }

    public UploadAdminPhotoException(String message, Throwable cause) {
        super(message, cause);
    }

    public UploadAdminPhotoException(Throwable cause) {
        super(cause);
    }

    public UploadAdminPhotoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
